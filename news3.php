        <h1>Common Dental Problems and How to Prevent Them</h1>
        <p class="intro">
            Oral health plays a crucial role in overall well-being, yet many people experience common dental issues that can be prevented 
            with proper care and hygiene. Understanding these problems and taking proactive steps can help maintain a healthy, beautiful smile.
        </p>

        <h2>1. Tooth Decay (Cavities)</h2>
        <p>Tooth decay is one of the most common dental problems worldwide. It occurs when plaque, a sticky film of bacteria, produces acids 
            that erode tooth enamel.</p>
        <h3>Prevention Tips:</h3>
        <ul>
            <li>Brush twice a day with fluoride toothpaste.</li>
            <li>Floss daily to remove plaque between teeth.</li>
            <li>Limit sugary and acidic foods and beverages.</li>
            <li>Visit your dentist for regular checkups and cleanings.</li>
        </ul>

        <h2>2. Gum Disease (Gingivitis & Periodontitis)</h2>
        <p>Gum disease is an infection of the gums caused by plaque buildup. If untreated, it can lead to tooth loss and other health complications.</p>
        <h3>Prevention Tips:</h3>
        <ul>
            <li>Maintain good oral hygiene by brushing and flossing daily.</li>
            <li>Use an antibacterial mouthwash to reduce plaque buildup.</li>
            <li>Quit smoking, as it increases the risk of gum disease.</li>
            <li>Schedule regular dental cleanings to detect early signs.</li>
        </ul>

        <h2>3. Bad Breath (Halitosis)</h2>
        <p>Persistent bad breath can be caused by poor oral hygiene, gum disease, dry mouth, or certain foods.</p>
        <h3>Prevention Tips:</h3>
        <ul>
            <li>Brush and floss daily to remove food particles and bacteria.</li>
            <li>Stay hydrated and chew sugar-free gum to stimulate saliva production.</li>
            <li>Clean your tongue regularly to remove bacteria.</li>
            <li>Visit a dentist to rule out underlying issues.</li>
        </ul>

        <h2>4. Tooth Sensitivity</h2>
        <p>Tooth sensitivity occurs when the protective enamel wears down, exposing the dentin layer underneath.</p>
        <h3>Prevention Tips:</h3>
        <ul>
            <li>Use a soft-bristled toothbrush and avoid aggressive brushing.</li>
            <li>Switch to toothpaste designed for sensitive teeth.</li>
            <li>Avoid extremely hot or cold foods and beverages.</li>
            <li>Seek dental treatment if sensitivity persists.</li>
        </ul>

        <h2>5. Teeth Grinding (Bruxism)</h2>
        <p>Grinding or clenching your teeth, often unconsciously, can cause enamel wear, jaw pain, and headaches.</p>
        <h3>Prevention Tips:</h3>
        <ul>
            <li>Use a custom-fitted mouthguard if you grind your teeth at night.</li>
            <li>Manage stress through relaxation techniques like meditation.</li>
            <li>Avoid caffeine and alcohol, which can contribute to grinding.</li>
            <li>Consult a dentist if the issue persists.</li>
        </ul>

        <h2>6. Stained or Discolored Teeth</h2>
        <p>Tooth discoloration can result from diet, smoking, or poor oral hygiene.</p>
        <h3>Prevention Tips:</h3>
        <ul>
            <li>Limit coffee, tea, and red wine consumption.</li>
            <li>Quit smoking to prevent staining.</li>
            <li>Brush with a whitening toothpaste and visit your dentist for professional cleaning.</li>
        </ul>

