        <h1>Simple Lifestyle Changes for a Healthier Heart</h1>
        <p class="intro">
            Heart disease remains one of the leading causes of illness worldwide, but the good news is that small, 
            consistent lifestyle changes can significantly reduce your risk. By making heart-healthy choices in daily life, 
            you can improve cardiovascular wellness and enhance overall well-being. Here are some simple yet effective habits 
            that support a healthy heart.
        </p>

        <h2>1. Eat a Heart-Healthy Diet</h2>
        <p>A balanced diet is essential for maintaining cardiovascular health. Incorporate:</p>
        <ul>
            <li>Fruits and Vegetables - Rich in antioxidants and fiber, they help lower blood pressure.</li>
            <li>Whole Grains - Brown rice, oats, and whole wheat improve cholesterol levels.</li>
            <li>Lean Proteins - Fish, poultry, and plant-based proteins reduce saturated fat intake.</li>
            <li>Healthy Fats- Nuts, seeds, and olive oil support heart function.</li>
        </ul>
        <p>Limiting processed foods, excess salt, and sugary beverages can prevent high blood pressure and reduce 
            the risk of heart disease.</p>

        <h2>2. Stay Active Every Day</h2>
        <p>Regular exercise strengthens the heart and improves circulation. Aim for:</p>
        <ul>
            <li>At least 30 minutes of moderate exercise (e.g., walking, cycling, swimming) most days of the week.</li>
            <li>Strength training to maintain muscle mass and support overall health.</li>
            <li>Simple movement habits such as taking the stairs or stretching throughout the day.</li>
        </ul>

        <h2>3. Manage Stress Effectively</h2>
        <p>Chronic stress can lead to high blood pressure and other heart-related problems. Try:</p>
        <ul>
            <li>Deep breathing and meditation to relax your body.</li>
            <li>Spending time with loved ones to enhance emotional well-being.</li>
            <li>Getting adequate sleep (7-8 hours per night) to allow the heart to recover and function optimally.</li>
        </ul>

        <h2>4. Monitor Your Blood Pressure and Cholesterol</h2>
        <p>Keeping an eye on blood pressure, cholesterol, and blood sugar levels helps detect potential heart problems early. 
            Regular health checkups at Care Compass Hospital provide expert guidance and preventive care tailored to your needs.</p>

        <h2>5. Quit Smoking and Limit Alcohol</h2>
        <p>Smoking damages blood vessels and significantly increases the risk of heart disease. Quitting smoking immediately benefits your heart health. 
            Additionally, limiting alcohol intake supports heart function and overall well-being.</p>

        <h2>Why Choose Care Compass Hospital for Cardiac Health?</h2>
